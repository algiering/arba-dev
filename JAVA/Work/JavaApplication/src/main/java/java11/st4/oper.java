package java11.st4;

public class oper {

    static int Add(int a, int b) {
        return (a + b);
    }

    static int Minus(int a, int b) {
        return (a - b);
    }

    static int Mul(int a, int b) {
        return (a * b);
    }

    static double Div(double a, double b) {
        return (a / b);
    }

}
